            <section id="faqs">
                <div class="wrap">

                    <h3>FAQs</h3>

                    <div id="accordion">
                        <h4 class="accordion-toggle">1. How does the treadmill cause repetitive use injuries? How does the trednatural correct this problem?</h4>
                        <div class="accordion-content default">
                            <p>Treadmills can cause repetitive use injuries by the same small area of the joints or bones being utilized with each step because of their flat surface. These injuries can include hip and medial knee pain, stress fractures, and plantar fasciitis. The TredNatural corrects this problem by shifting the treadmill right and left, in turn shifting the weight around the joints.</p>
                        </div>

                        <h4 class="accordion-toggle">2. Where did the idea for the TredNatural come from?</h4>
                        <div class="accordion-content">
                            <p>Dr. John Stelmach, a practicing orthopaedic surgeon for 30 years, and his sister Dr. Dee Stelmach, a practicing podiatrist for 40 years, came up with the idea while walking outside. You will notice that while walking in the snow or on the beach that your tracks are curved. Because the world is round, you are unknowingly adjusting to the curved, uneven surface below you. Therefore, the TredNatural was created to mimic that same feeling of walking outside. </p>
                        </div>

                        <h4 class="accordion-toggle">3. How can the TredNatural help athletes and professional athletes?</h4>
                        <div class="accordion-content">
                            <p>Athletes will often stress any machine they are working with to the limit. Running on the same repetitive, flat surface can cause repetitive use injuries, and recognizing this, treadmills are often not an athlete's preferred training method of choice. With the TredNatural, athletes would be able to use treadmills without discomfort and have less chance of injury, with the benefit of being able to train inside.</p>
                        </div>

                        <h4 class="accordion-toggle">4. Is the TredNatural for everyone?</h4>
                        <div class="accordion-content">
                            <p>Absolutely! The TredNatural can help everyone who wants to stay fit and get a workout in the comfort of their own home. The TredNatural can also help with core strength better than a regular treadmill. Many people have commented about the workout of their core after using the TredNatural as they are constantly balancing on the shifting surface. Not to mention, it will lower the chance of repetitive use discomfort and injury.</p>
                        </div>
